// tipos de declaração

// function declaration
function soma(x, y){
    return x + y
}
console.log(soma(3, 5))

// function expression
const sub = function(x, y){
    return x - y
}
console.log(sub(5, 3))

// named function expression
const mult = function mult(x, y){
    return x * y
}
console.log(mult(3, 5))